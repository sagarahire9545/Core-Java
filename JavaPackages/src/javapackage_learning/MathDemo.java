package javapackage_learning;

public class MathDemo {

	public static void main(String[] args)
	{
		System.out.print("Absolute:");
		System.out.println(Math.abs(-127));
		
		System.out.print("Absolute:");
		System.out.println(StrictMath.abs(-127));
		
		System.out.print("Cube root:");
		System.out.println(Math.cbrt(9));
		
		System.out.print("Floor Division:");
        System.out.println(Math.floorDiv(50, 9));
        
        System.out.print("Max no:");
        System.out.println(Math.max(20, 78));
        
        System.out.print("Radiu:");
        System.out.println(Math.toRadians(25));
		
		
	}

}
